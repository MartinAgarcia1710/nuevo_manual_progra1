# -*- coding: utf-8 -*-
PI = 3.14159

def area_cuadrado(lado):
    """Devuelve el área de un cuadrado dado su lado."""
    return lado ** 2

def perimetro_cuadrado(lado):
    """Devuelve el perímetro de un cuadrado dado su lado."""
    return lado * 4

def area_circulo(radio):
    """Devuelve el área de un círculo dado su radio."""
    return PI * radio ** 2
